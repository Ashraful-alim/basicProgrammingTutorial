# Problem :
# Create a Python program that calculates the area of a rectangle. 
# The program should prompt the user to enter the length and width of the rectangle, 
# store the values in variables, and then calculate and display the area.

# Answer :

length = float(input("Enter the length of the rectangle: "))
width = float(input("Enter the width of the rectangle: "))

area = length * width

print("The area of the rectangle is:", area)
